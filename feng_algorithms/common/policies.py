class GNN_Layer(nn.Module):
    def __init__(
                self,
                in_feat,
                out_feat,
                graph,
            ):      
        super(GNN_Layer, self).__init__()
        self.g = graph
        self.in_feat = in_feat
        
        self.loop_weight =nn.Parameter(th.Tensor(graph.num_nodes(), in_feat, out_feat))
        self.W = nn.Parameter(th.Tensor(graph.num_edges(), in_feat, out_feat))
        self.m_bias = nn.Parameter(th.Tensor(graph.num_edges(), 1, out_feat)) # useless
        self.h_bias = nn.Parameter(th.Tensor(graph.num_nodes(), 1, out_feat)) # useless
        self._reset_parameters()

    def _reset_parameters(self):
        nn.init.xavier_uniform_(self.loop_weight, gain=nn.init.calculate_gain('tanh'))
        nn.init.uniform_(self.W, -1/math.sqrt(self.in_feat), 1/math.sqrt(self.in_feat))
        nn.init.zeros_(self.m_bias)
        nn.init.zeros_(self.h_bias)

    def message(self, edges):
        if edges.src['h'].dim() == 2:
            x = edges.src['h'].view(-1, 1, self.in_feat)
            message = th.bmm(x, self.W) + self.m_bias
        elif edges.src['h'].dim() == 3:
            message = th.bmm(edges.src['h'], self.W) + self.m_bias
        return {'m' : message}

    def forward(self, feat):
        with self.g.local_scope():
            self.g.srcdata['h'] = feat # 4, 6, 6, target, t, t-1, t-2

            # self-loop
            if self.g.srcdata['h'].dim() == 2:
                x = self.g.srcdata['h'].view(-1, 1, self.in_feat)
                loop = th.bmm(x,  self.loop_weight)
            elif self.g.srcdata['h'].dim() == 3:
                loop = th.bmm(self.g.srcdata['h'], self.loop_weight)
            self.g.update_all(self.message, fn.sum('m', 'h'))
            h = self.g.dstdata['h'] + self.h_bias + loop # 4, 6, 8, node * batch * dim
            return h.squeeze()

class GNN(nn.Module):
    def __init__(
                self,
                in_feat,
                out_feat,
                graph,
            ):
        super(GNN, self).__init__()
        self.layer1 = GNN_Layer(in_feat, 8, graph)
        self.layer2 = GNN_Layer(8, out_feat, graph)

    def forward(self, features):
        x = th.tanh(self.layer1(features))
        x = self.layer2(x)
        return x

class ActorCriticGnnPolicy(BasePolicy):

    def __init__(
        self,
        observation_space: gym.spaces.Space,
        action_space: gym.spaces.Space,
        lr_schedule: Schedule,
        net_arch: Optional[List[Union[int, Dict[str, List[int]]]]] = None,
        activation_fn: Type[nn.Module] = nn.Tanh,
        ortho_init: bool = True,
        use_sde: bool = False,
        log_std_init: float = 0.0,
        full_std: bool = True,
        sde_net_arch: Optional[List[int]] = None,
        use_expln: bool = False,
        squash_output: bool = False,
        features_extractor_class: Type[BaseFeaturesExtractor] = FlattenExtractor,
        features_extractor_kwargs: Optional[Dict[str, Any]] = None,
        normalize_images: bool = True,
        optimizer_class: Type[th.optim.Optimizer] = th.optim.Adam,
        optimizer_kwargs: Optional[Dict[str, Any]] = None,
    ):

        if optimizer_kwargs is None:
            optimizer_kwargs = {}
            # Small values to avoid NaN in Adam optimizer
            if optimizer_class == th.optim.Adam:
                optimizer_kwargs["eps"] = 1e-5

        super().__init__(
            observation_space,
            action_space,
            features_extractor_class,
            features_extractor_kwargs,
            optimizer_class=optimizer_class,
            optimizer_kwargs=optimizer_kwargs,
            squash_output=squash_output,
        )

        # Default network architecture, from stable-baselines
        if net_arch is None:
            if features_extractor_class == NatureCNN:
                net_arch = []
            else:
                net_arch = [dict(pi=[64, 64], vf=[64, 64])]

        self.net_arch = net_arch
        self.activation_fn = activation_fn
        self.ortho_init = ortho_init
        
        self.features_extractor = features_extractor_class(self.observation_space, **self.features_extractor_kwargs)
        # self.features_dim = self.features_extractor.features_dim
        self.features_dim = 32

        self.normalize_images = normalize_images
        self.log_std_init = log_std_init
        dist_kwargs = None
        # Keyword arguments for gSDE distribution
        if use_sde:
            dist_kwargs = {
                "full_std": full_std,
                "squash_output": squash_output,
                "use_expln": use_expln,
                "learn_features": False,
            }

        if sde_net_arch is not None:
            warnings.warn("sde_net_arch is deprecated and will be removed in SB3 v2.4.0.", DeprecationWarning)

        self.use_sde = use_sde
        self.dist_kwargs = dist_kwargs

        # Action distribution
        self.action_dist = make_proba_distribution(action_space, use_sde=use_sde, dist_kwargs=dist_kwargs)

        src_ids = th.tensor([0, 0, 0, 1, 2, 1])
        dst_ids = th.tensor([1, 2, 3, 2, 3, 3])
        device = th.device("cuda:0" if th.cuda.is_available() else "cpu")
        self.g = dgl.graph((src_ids, dst_ids)).to(device)
        
        self.gnn = GNN(6, 8, self.g)

        self._build(lr_schedule)

    def _get_constructor_parameters(self) -> Dict[str, Any]:
        data = super()._get_constructor_parameters()

        default_none_kwargs = self.dist_kwargs or collections.defaultdict(lambda: None)

        data.update(
            dict(
                net_arch=self.net_arch,
                activation_fn=self.activation_fn,
                use_sde=self.use_sde,
                log_std_init=self.log_std_init,
                squash_output=default_none_kwargs["squash_output"],
                full_std=default_none_kwargs["full_std"],
                use_expln=default_none_kwargs["use_expln"],
                lr_schedule=self._dummy_schedule,  # dummy lr schedule, not needed for loading policy alone
                ortho_init=self.ortho_init,
                optimizer_class=self.optimizer_class,
                optimizer_kwargs=self.optimizer_kwargs,
                features_extractor_class=self.features_extractor_class,
                features_extractor_kwargs=self.features_extractor_kwargs,
            )
        )
        return data

    def reset_noise(self, n_envs: int = 1) -> None:
        """
        Sample new weights for the exploration matrix.

        :param n_envs:
        """
        assert isinstance(self.action_dist, StateDependentNoiseDistribution), "reset_noise() is only available when using gSDE"
        self.action_dist.sample_weights(self.log_std, batch_size=n_envs)

    def _build_mlp_extractor(self) -> None:
        """
        Create the policy and value networks.
        Part of the layers can be shared.
        """
        # Note: If net_arch is None and some features extractor is used,
        #       net_arch here is an empty list and mlp_extractor does not
        #       really contain any layers (acts like an identity module).
        self.mlp_extractor = MlpExtractor(
            self.features_dim,
            net_arch=self.net_arch,
            activation_fn=self.activation_fn,
            device=self.device,
        )

    def _build(self, lr_schedule: Schedule) -> None:
        """
        Create the networks and the optimizer.

        :param lr_schedule: Learning rate schedule
            lr_schedule(1) is the initial learning rate
        """
        self._build_mlp_extractor()

        latent_dim_pi = self.mlp_extractor.latent_dim_pi

        if isinstance(self.action_dist, DiagGaussianDistribution):
            self.action_net, self.log_std = self.action_dist.proba_distribution_net(
                latent_dim=latent_dim_pi, log_std_init=self.log_std_init
            )
        elif isinstance(self.action_dist, StateDependentNoiseDistribution):
            self.action_net, self.log_std = self.action_dist.proba_distribution_net(
                latent_dim=latent_dim_pi, latent_sde_dim=latent_dim_pi, log_std_init=self.log_std_init
            )
        elif isinstance(self.action_dist, (CategoricalDistribution, MultiCategoricalDistribution, BernoulliDistribution)):
            self.action_net = self.action_dist.proba_distribution_net(latent_dim=latent_dim_pi)
        else:
            raise NotImplementedError(f"Unsupported distribution '{self.action_dist}'.")

        self.value_net = nn.Linear(self.mlp_extractor.latent_dim_vf, 1)
        # Init weights: use orthogonal initialization
        # with small initial weight for the output
        if self.ortho_init:
            # TODO: check for features_extractor
            # Values from stable-baselines.
            # features_extractor/mlp values are
            # originally from openai/baselines (default gains/init_scales).
            module_gains = {
                self.mlp_extractor: np.sqrt(2),
                self.action_net: 0.01,
                self.value_net: 1,
            }
            for module, gain in module_gains.items():
                module.apply(partial(self.init_weights, gain=gain))

        # Setup optimizer with initial learning rate
        self.optimizer = self.optimizer_class(self.parameters(), lr=lr_schedule(1), **self.optimizer_kwargs)

    def forward(self, obs: th.Tensor, t_1_info: th.Tensor, t_2_info: th.Tensor, deterministic: bool = False) -> Tuple[th.Tensor, th.Tensor, th.Tensor]:
        """
        Forward pass in all the networks (actor and critic)

        :param obs: Observation
        :param deterministic: Whether to sample or use deterministic actions
        :return: action, value and log probability of the action
        """
        # Preprocess the observation if needed
        # features = self.extract_features(obs)
        if obs.dim() == 2:
            features = self.batch_gnn_process(obs, t_1_info, t_2_info)
        else:
            features = self.gnn_process(obs, t_1_info, t_2_info)
        latent_pi, latent_vf = self.mlp_extractor(features)
        # Evaluate the values for the given observations
        values = self.value_net(latent_vf)
        distribution = self._get_action_dist_from_latent(latent_pi)
        actions = distribution.get_actions(deterministic=deterministic)
        log_prob = distribution.log_prob(actions)
        return actions, values, log_prob

    def _get_action_dist_from_latent(self, latent_pi: th.Tensor) -> Distribution:
        """
        Retrieve action distribution given the latent codes.

        :param latent_pi: Latent code for the actor
        :return: Action distribution
        """
        mean_actions = self.action_net(latent_pi)

        if isinstance(self.action_dist, DiagGaussianDistribution):
            return self.action_dist.proba_distribution(mean_actions, self.log_std)
        elif isinstance(self.action_dist, CategoricalDistribution):
            # Here mean_actions are the logits before the softmax
            return self.action_dist.proba_distribution(action_logits=mean_actions)
        elif isinstance(self.action_dist, MultiCategoricalDistribution):
            # Here mean_actions are the flattened logits
            return self.action_dist.proba_distribution(action_logits=mean_actions)
        elif isinstance(self.action_dist, BernoulliDistribution):
            # Here mean_actions are the logits (before rounding to get the binary actions)
            return self.action_dist.proba_distribution(action_logits=mean_actions)
        elif isinstance(self.action_dist, StateDependentNoiseDistribution):
            return self.action_dist.proba_distribution(mean_actions, self.log_std, latent_pi)
        else:
            raise ValueError("Invalid action distribution")

    def _predict(self, observation: th.Tensor, deterministic: bool = False) -> th.Tensor:
        """
        Get the action according to the policy for a given observation.

        :param observation:
        :param deterministic: Whether to use stochastic or deterministic actions
        :return: Taken action according to the policy
        """
        return self.get_distribution(observation).get_actions(deterministic=deterministic)

    def evaluate_actions(self, obs: th.Tensor, t_1_info: th.Tensor, t_2_info: th.Tensor, actions: th.Tensor) -> Tuple[th.Tensor, th.Tensor, th.Tensor]:
        """
        Evaluate actions according to the current policy,
        given the observations.

        :param obs:
        :param actions:
        :return: estimated value, log likelihood of taking those actions
            and entropy of the action distribution.
        """
        # Preprocess the observation if needed
        if obs.dim() == 2:
            features = self.batch_gnn_process(obs, t_1_info, t_2_info)
        else:
            features = self.gnn_process(obs, t_1_info, t_2_info)
        # features = self.extract_features(obs)
        latent_pi, latent_vf = self.mlp_extractor(features)
        distribution = self._get_action_dist_from_latent(latent_pi)
        log_prob = distribution.log_prob(actions)
        values = self.value_net(latent_vf)
        return values, log_prob, distribution.entropy()

    def get_distribution(self, obs, t_1_info, t_2_info: th.Tensor) -> Distribution:
        """
        Get the current policy distribution given the observations.

        :param obs:
        :return: the action distribution.
        """
        # features = self.extract_features(obs)
        if obs.dim() == 2:
            features = self.batch_gnn_process(obs, t_1_info, t_2_info)
        else:
            features = self.gnn_process(obs, t_1_info, t_2_info)
        latent_pi = self.mlp_extractor.forward_actor(features)
        return self._get_action_dist_from_latent(latent_pi)

    def predict_values(self, obs: th.Tensor, t_1_info: th.Tensor, t_2_info: th.Tensor) -> th.Tensor:
        """
        Get the estimated values according to the current policy given the observations.

        :param obs:
        :return: the estimated values.
        """
        # features = self.extract_features(obs)
        if obs.dim() == 2:
            features = self.batch_gnn_process(obs, t_1_info, t_2_info)
        else:
            features = self.gnn_process(obs, t_1_info, t_2_info)
        latent_vf = self.mlp_extractor.forward_critic(features)
        return self.value_net(latent_vf)

    def batch_gnn_process(self, obss, t_1_infos, t_2_infos):
        target_poss = obss[:,6:]
        target_infos = th.cat((target_poss,th.zeros_like(target_poss),th.zeros_like(target_poss)),dim=1).squeeze()
        nodes_infos = th.stack((target_infos, t_1_infos, t_2_infos, obss[:, 0: 6].squeeze()),dim=1) # 6, 4, 6
        nodes_infos = th.transpose(nodes_infos, 0, 1).float() # 4, 6, 6, nodes, batch, info
        graph_output = th.tanh(self.gnn(nodes_infos)) # 4, 6, 8
        graph_output = th.transpose(graph_output, 0, 1) # 6, 4, 8
        features = th.flatten(graph_output, start_dim=1) # 6, 32
        return features     

    def gnn_process(self, obs, t_1_info, t_2_info):
        target_pos = obs[6:]
        target_info = th.cat((target_pos,th.zeros_like(target_pos),th.zeros_like(target_pos)))
        node_info = th.cat((target_info, t_1_info.squeeze(), t_2_info.squeeze(), obs[0: 6])).view(4, 6).float() # 4, 6
        features = th.tanh(self.gnn(node_info)).flatten() # 4, 6 --> 4, 8 --> 32
        return features   

